/** Strips SRT indexes/timestamps, leaving one caption line per row. */
function srtToPlainText(srt: string): string {
  return srt
    .split(/\r?\n/)
    .filter(
      (line) =>
        line.trim().length > 0 &&
        !/^\d+$/.test(line.trim()) &&
        !line.includes("-->"),
    )
    .join("\n");
}
