import { createPrivateTool } from "@decocms/runtime/tools";
import { z } from "zod";
import {
  getInnertube,
  parseVideoId,
  wrapInnertubeError,
} from "../lib/innertube.ts";
import type { Env } from "../types/env.ts";

const CaptionTrackSchema = z.object({
  languageCode: z.string(),
  name: z.string(),
  isAutoGenerated: z.boolean(),
  isTranslatable: z.boolean(),
});

const FormatSchema = z.object({
  itag: z.number(),
  qualityLabel: z.string().optional(),
  mimeType: z.string(),
  hasAudio: z.boolean(),
  hasVideo: z.boolean(),
  bitrate: z.number().optional(),
  contentLengthBytes: z.number().optional(),
  fps: z.number().optional(),
});

// deno-lint-ignore no-explicit-any
export function mapCaptionTracks(info: any) {
  const tracks: unknown[] = info?.captions?.caption_tracks ?? [];
  // deno-lint-ignore no-explicit-any
  return tracks.map((track: any) => ({
    languageCode: String(track?.language_code ?? ""),
    name: track?.name?.toString?.() ?? "",
    // ASR (auto-generated) tracks are marked with kind "asr" / vss_id "a.<lang>"
    isAutoGenerated:
      track?.kind === "asr" || String(track?.vss_id ?? "").startsWith("a."),
    isTranslatable: Boolean(track?.is_translatable),
  }));
}

export const createGetVideoDetailsTool = (_env: Env) =>
  createPrivateTool({
    id: "YOUTUBE_GET_VIDEO_DETAILS",
    description:
      "Get full metadata for a YouTube video: complete description, stats (views/likes), channel, keywords, available caption languages and downloadable formats. Accepts a video id or any YouTube URL.",
    inputSchema: z.object({
      videoId: z
        .string()
        .describe("YouTube video id (11 chars) or full video URL"),
    }),
    outputSchema: z.object({
      videoId: z.string(),
      url: z.string(),
      title: z.string(),
      description: z.string(),
      channelId: z.string().optional(),
      channelName: z.string().optional(),
      viewCount: z.number().optional(),
      likeCount: z.number().optional(),
      publishedText: z.string().optional(),
      durationSeconds: z.number().optional(),
      keywords: z.array(z.string()),
      category: z.string().optional(),
      isLive: z.boolean(),
      isFamilySafe: z.boolean().optional(),
      thumbnailUrl: z.string().optional(),
      captionTracks: z.array(CaptionTrackSchema),
      hasTranscript: z
        .boolean()
        .describe("Whether YOUTUBE_GET_TRANSCRIPT will work for this video"),
      availableFormats: z
        .array(FormatSchema)
        .describe(
          "Streams available to YOUTUBE_DOWNLOAD_VIDEO. Formats with hasAudio && hasVideo are muxed (downloadable as-is); others are DASH audio-only/video-only.",
        ),
    }),
    execute: async ({ context }) => {
      const videoId = parseVideoId(context.videoId);
      const yt = await getInnertube();

      let info;
      try {
        info = await yt.getInfo(videoId);
      } catch (error) {
        throw wrapInnertubeError(error);
      }

      const basic = info.basic_info;
      const streamingData = info.streaming_data;
      // deno-lint-ignore no-explicit-any
      const allFormats: any[] = [
        ...(streamingData?.formats ?? []),
        ...(streamingData?.adaptive_formats ?? []),
      ];

      const captionTracks = mapCaptionTracks(info);

      return {
        videoId,
        url: `https://www.youtube.com/watch?v=${videoId}`,
        title: basic.title ?? "",
        description: basic.short_description ?? "",
        channelId: basic.channel_id ?? basic.channel?.id,
        channelName: basic.channel?.name ?? basic.author,
        viewCount: basic.view_count ?? undefined,
        likeCount: basic.like_count ?? undefined,
        publishedText:
          // deno-lint-ignore no-explicit-any
          (info as any).primary_info?.published?.toString?.() ?? undefined,
        durationSeconds: basic.duration ?? undefined,
        keywords: basic.keywords ?? [],
        category: basic.category ?? undefined,
        isLive: Boolean(basic.is_live),
        isFamilySafe: basic.is_family_safe ?? undefined,
        thumbnailUrl: basic.thumbnail?.[0]?.url,
        captionTracks,
        hasTranscript: captionTracks.length > 0,
        availableFormats: allFormats.map((format) => ({
          itag: Number(format?.itag ?? 0),
          qualityLabel: format?.quality_label ?? undefined,
          mimeType: String(format?.mime_type ?? ""),
          hasAudio: Boolean(format?.has_audio),
          hasVideo: Boolean(format?.has_video),
          bitrate: format?.bitrate ?? undefined,
          contentLengthBytes:
            format?.content_length != null
              ? Number(format.content_length)
              : undefined,
          fps: format?.fps ?? undefined,
        })),
      };
    },
  });
